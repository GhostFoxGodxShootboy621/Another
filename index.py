Number = int(input("Enter a number: "))
print(Number) # prints enter no.
print(Number + 10) # prints enter no. + 10
print(Number - 10) # prints enter no. - 10
print(Number * 10) # prints enter no. * 10
print(Number / 10) # prints enter no. / 10
print(Number % 10) # prints enter no. % 10
print(Number ** 10) # prints enter no. ** 10
print(Number // 10) # prints enter no. // 10

